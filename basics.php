    	<p class="question">What is copyright?</p>
		<p>Copyright is a set of laws designed to give creators a package of rights over their original works of authorship. These rights include:</p>
		<ul>
			<li>Creating derivative works or translations</li>
			<li>Reproducing the work in copies</li>
			<li>Distributing copies</li>
			<li>Performing or displaying publically or digitally</li>
			<li>Authorizing others to exercise any of these rights</li>
		</ul>
		<p>Copyright protects and balances the rights of the author and the public. The law recognizes that creative works are unique, intellectual property with potential commerical value. Additionally, it provides a legal framework to recover damages in cases of infringement and it provides an outline of acceptable methods of using protected works for many purposes.</p>

		<p class="question">What does copyright protect?</p>
		<p>According to the U.S. Copyright Office, “copyright protects ‘original works of authorship’ that are fixed in a tangible form of expression.” In other words, the work must be written or recorded in some form to be protected. The following types of works are identified:</p>
		<ul>
			<li>Literary works</li>
			<li>Musical works, including any accompanying words</li>
			<li>Dramatic works, including any accompanying music</li>
			<li>Pantomimes and choreographic works</li>
			<li>Pictorial, graphic, and sculptural works</li>
			<li>Motion pictures and other audiovisual works</li>
			<li>Sound recordings</li>
			<li>Architectural works</li>
		</ul>
		
		<p class="question">What does copyright not protect?</p>
		<p>Copyright does not protect the following:</p>
		<ul>
			<li>Works not fixed in a tangible form</li>
			<li>Ideas, facts or data</li>
			<li>Titles, names or slogans</li>
			<li>Works of the U.S. Government</li>
			<li>Works in the Public Domain</li>
		</ul>

		<p class="question">What is the Public Domain?</p>
		<p>The Public Domain refers to works that were created in certain time periods that are no longer protected by copyright. The following works are in the Public Domain:</p>
		<ul>
			<li>Works published prior to 1923</li>
			<li>Works published between 1923 and 1963 without a copyright notice</li>
			<li>Works published between 1923 and 1963 with a copyright notice but without the copyright being renewed</li>
		</ul>
		<p>*<a href="http://www.copyright.gov" target="_blank">www.copyright.gov</a> is a source to check the copyright status of works. Renewed works will be registered here. Please note: not all renewal records are available electronically.</p>
