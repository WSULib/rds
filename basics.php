    	<p class="question">What is the T.E.A.C.H. Act?</p>
		<p>In 2002, Congress passed the Technology, Education, and Copyright Harmonization (T.E.A.C.H.) Act to address copyright concerns in online distance learning and course management systems. Previously, copyright law strictly limited educators’ ability to remain compliant in an online setting. The T.E.A.C.H. Act expands rights in the following ways:</p>
		<ul>
			<li>Transmission of performances of the entirety of a non-dramatic literary or musical work</li>
			<li>Transmission of reasonable and limited portions of any other performance</li>
			<li>Transmission of any work in amounts comparable to typical face-to-face displays</li>
		</ul>
		<p>In order to be T.E.A.C.H. Act compliant, there is a formidable list of conditions that must be met by the educational institution. At this time, Wayne State University has not implemented the T.E.A.C.H. Act and WSU faculty are not able to use the act to justify their use of copyright materials within their online instruction.</p>
		<p>For more information on the T.E.A.C.H. Act, please see <a href="http://www.ala.org/advocacy/copyright/teachact/faq" target="_blank">The TEACH Act and some Frequently Asked Questions</a> as prepared by the <a href="http://www.ala.org/" target="_blank">American Library Association</a>.</p>
