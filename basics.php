    	<p class="question">What is Fair Use?</p>
		<p>As previously mentioned each use must be analyzed on a case-by-case basis, but in general, Fair Use protects criticism, comment, news reporting, teaching, scholarship, and research.</p>
		<p>There are four factors that must be considered in determining whether or not a particular use is fair. These factors are:</p>
		<ol>
			<li>The purpose and character of the use</li>
			<li>The nature of the copyrighted work</li>
			<li>The amount and substantiality of the portion used</li>
			<li>The effect of the use on the potential market for the copyright work</li>
		</ol>
		<p>For more detail concerning these four factors, as well as assistance in determining whether or not the use is fair, please see our <a href="checklist.php">Interactive Fair Use Checklist</a>.</p>

		<p class="question">When should I seek permission?</p>
		<p>You should seek copyright permission when you intend to use the work for commercial purposes, when you want to use a work in its entirety, or if the use does not comply with Fair Use statutes.</p>
		<p>The Wayne State University Library System can help you identify which materials are available through library resources, which materials would require permission to use, and help suggest alternative works that the library system owns. Simply submit your course reading list using our <a href="http://www.lib.wayne.edu/forms/persistent_link_request.php" target="_blank">Persistent Link Request</a> form and a librarian will assist you.</p>

		<p class="question">How do I obtain permission?</p>
		<p>Once you have determined that you need to obtain copyright permission to use a work, search the Copyright Clearance Center at <a href="http://www.copyright.com" target="_blank">www.copyright.com</a>.</p>
		<p>If you would like further assistance, contact your <a href="http://www.lib.wayne.edu/info/staff/liaison.php" target="_blank">librarian liaison</a> or <a href="mailto:copyright@wayne.edu">copyright@wayne.edu</a>.</p>
