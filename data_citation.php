<p>
    <h2>Rationale:</h2> <p>The motivation for citing datasets and linking them to publications is: 1) to assign due credit for the data, 2) to aid readers in locating and accessing the data, and 3) to help track the impact of the data.</p>
</p>

<p>
<h2>Elements:</h2> <p>A robust citation should uniquely identify the object cited, and should enable digital access to the object.  The basic elements that should be present in any citation are: author(s), title and date, and location.</p>
</p>

<p>
<h2>Formal identifiers:</h2> <p>While it should be possible to uniquely identify a dataset from these elements, in practice a formal identifier (like DOI) is used to enable unambiguous identification of the dataset using a resolver service (like http://dx.doi.org). Data repositories will generally assign a formal/persistent identifier to your dataset at your request prior to deposit.</p>
</p>

<p>
<h2>Examples:</h2>
<p>Cheng, D. W., Jiang, Y., Shalev, A., Kowluru, R., Crook, E. D., & Singh, L. P. (2012). <em>Transcription profiling by array of mouse MES-13 cells after treatment with glucose or glucosamine</em> [scan; processed Data Matrix]. Array Express – EMBL/EBI.  <a href="http://www.ebi.ac.uk/arrayexpress/experiments/E-GEOD-2557">http://www.ebi.ac.uk/arrayexpress/experiments/E-GEOD-2557</a></p>
<p>Tennstedt, Sharon, Morris, John, Unverzagt, Frederick, Rebok, George, Willis, Sherry, Ball, Karlene, and Marsiske, Michael. (2005). <em>ACTIVE (Advanced Cognitive Training for Independent and Vital Elderly)</em>, 1999-2001 [United States]. ICPSR - Interuniversity Consortium for Political and Social Research. doi:10.3886/ICPSR04248</p>
</p>

<p>
<h2>Usage:</h2> Cite datasets using the style and elements required by the journal editor/publisher, or adapt a standard data citation style (such as <a href="http://www.datacite.org/whycitedata">DataCite</a>) to match the style of the journal; include data citations in the reference list alongside textual sources; cite the dataset at the level of granularity that best suits your needs.
</p>

<p>
<h2>Tools:</h2> <p>The <a href="http://crosscite.org/citeproc/">DataCite citation formatter</a> will accept a DOI as input, and produce a data citation in a number of common styles.</p>
</p>

